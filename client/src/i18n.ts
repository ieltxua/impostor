type MessageKey = keyof (typeof messages)['en'];

const getStoredLocale = (): Locale | null => {
  if (typeof window === 'undefined' || typeof localStorage === 'undefined') {
    return null;
  }

  const raw = localStorage.getItem(LOCALE_STORAGE_KEY);
  return normalizeLocale(raw);
};

const getQueryLocale = (): Locale | null => {
  if (typeof window === 'undefined') {
    return null;
  }

  const raw = new URLSearchParams(window.location.search).get(LOCALE_QUERY_PARAM);
  return normalizeLocale(raw);
};

const normalizeLocale = (value: string | null): Locale | null =>
  value && LOCALES.includes(value as Locale) ? (value as Locale) : null;

let locale: Locale = getQueryLocale() ?? getStoredLocale() ?? DEFAULT_LOCALE;

const interpolate = (value: string, params?: Record<string, string | number>): string => {
  if (!params) {
    return value;
  }

  return value.replace(/\{(\w+)\}/g, (_match, key: string) =>
    Object.prototype.hasOwnProperty.call(params, key) ? String(params[key]) : `{${key}}`
  );
};

export const t = <K extends MessageKey>(key: K, params?: Record<string, string | number>): string =>
  interpolate(messages[locale][key], params);

export const setLocale = (next: Locale) => {
  locale = next;
  if (typeof localStorage !== 'undefined') {
    localStorage.setItem(LOCALE_STORAGE_KEY, next);
  }
};

export const getLocale = (): Locale => locale;
export const getDefaultLocale = (): Locale => DEFAULT_LOCALE;
export const formatStatus = (status: Status): string => t(`status.${status}` as MessageKey);
export const formatMode = (mode: Mode): string => t(`mode.${mode}` as MessageKey);
export const formatRole = (role: Role): string => t(`role.${role}` as MessageKey);
export const formatWinner = (winner: Winner): string => t(`winner.${winner}` as MessageKey);
